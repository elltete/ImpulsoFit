class Avance:
    
    def __init__(self, fecha, porcentaje_objetivo, sesiones_completas, detalle):        
        self.__fecha
        self.__porcentaje_objetivo
        self.__sesiones_completas
        self.__detalle

#Getters
    def get_fecha(self):
        return self.__fecha
    
    def get_porcentaje_objetivo(self):
        return self.__porcentaje_objetivo
    
    def get_sesiones_completas(self):
        return self.__sesiones_completas
    
    def get_detalle(self):
        return self.__detalle
    